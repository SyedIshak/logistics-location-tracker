package com.turvo.model;

/**
 * The Class Driver.
 */
public class Driver {

	/** The driver ID. */
	private String driverID;
	
	/** The driver name. */
	private String driverName;
	
	/** The driver contact. */
	private String driverContact;

	/**
	 * Gets the driver ID.
	 *
	 * @return the driver ID
	 */
	public String getDriverID() {
		return driverID;
	}

	/**
	 * Sets the driver ID.
	 *
	 * @param driverID the new driver ID
	 */
	public void setDriverID(String driverID) {
		this.driverID = driverID;
	}

	/**
	 * Gets the driver name.
	 *
	 * @return the driver name
	 */
	public String getDriverName() {
		return driverName;
	}

	/**
	 * Sets the driver name.
	 *
	 * @param driverName the new driver name
	 */
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}

	/**
	 * Gets the driver contact.
	 *
	 * @return the driver contact
	 */
	public String getDriverContact() {
		return driverContact;
	}

	/**
	 * Sets the driver contact.
	 *
	 * @param driverContact the new driver contact
	 */
	public void setDriverContact(String driverContact) {
		this.driverContact = driverContact;
	}

}
